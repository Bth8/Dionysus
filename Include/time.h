#ifndef TIME_H
#define TIME_H

#ifndef TIME_T
#define TIME_T
typedef unsigned int time_t;
#endif

struct tm {
	int tm_sec;
	int tm_min;
	int tm_hour;
	int tm_mday;
	int tm_mon;
	int tm_year;
	int tm_wday;
	int tm_yday;
	int tm_isdst;
};

time_t mktime(struct tm *tp);
struct tm *gmtime(const time_t *timer);
void init_time();

#endif
